// Helper to derive color family from title/description
const deriveColorFamily = (title: string, description: string = ''): ColorFamily => {
  const text = (title + ' ' + description).toLowerCase();
  if (text.includes('black') || text.includes('onyx') || text.includes('dark')) return 'black';
  if (text.includes('white') || text.includes('triple white') || text.includes('cream') || text.includes('ivory')) return 'white';
  if (text.includes('gray') || text.includes('grey') || text.includes('castlerock') || text.includes('steel')) return 'gray';
  if (text.includes('navy') || text.includes('night')) return 'navy';
  if (text.includes('brown') || text.includes('mocha') || text.includes('camel') || text.includes('cacao')) return 'brown';
  if (text.includes('beige') || text.includes('bone') || text.includes('sand') || text.includes('taupe') || text.includes('oatmeal')) return 'beige';
  if (text.includes('green') || text.includes('olive')) return 'green';
  if (text.includes('blue') || text.includes('azure') || text.includes('sky') || text.includes('tiffany')) return 'blue';
  if (text.includes('red') || text.includes('wine') || text.includes('maroon') || text.includes('burgundy')) return 'red';
  if (text.includes('pink') || text.includes('flamingo') || text.includes('peony')) return 'pink';
  if (text.includes('purple') || text.includes('plum')) return 'purple';
  if (text.includes('orange')) return 'orange';
  return 'black';
};

// Helper to derive style from brand/tags
const deriveStyle = (brand: string, tags: string[], subCategory: string): StyleType => {
  const brandLower = brand.toLowerCase();
  const tagString = tags.join(' ').toLowerCase();
  
  if (['jacquemus', 'omega', 'swatch x omega', 'amiri', 'ami paris', 'karl lagerfeld', 'balmain', 'gucci'].includes(brandLower)) return 'luxury';
  if (['fear of god', 'yeezy', 'supreme', 'kenzo'].includes(brandLower)) return 'streetwear';
  if (['on', 'adidas', 'nike'].includes(brandLower) && (tagString.includes('running') || tagString.includes('athletic'))) return 'athletic';
  if (['dickies', 'polo ralph lauren', 'all saints'].includes(brandLower)) return 'casual';
  if (tagString.includes('formal') || subCategory.includes('oxford')) return 'formal';
  if (brandLower.includes('minimal') || tagString.includes('minimal')) return 'minimalist';
  return 'casual';
};

// Helper to derive season from tags/description
const deriveSeason = (tags: string[], description: string = ''): Season => {
  const text = (tags.join(' ') + ' ' + description).toLowerCase();
  if (text.includes('winter') || text.includes('fleece') || text.includes('hoodie') || text.includes('sweater')) return 'winter';
  if (text.includes('summer') || text.includes('slides') || text.includes('tank')) return 'summer';
  return 'all';
};

