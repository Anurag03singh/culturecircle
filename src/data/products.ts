// Helper functions for filtering
export const getProductsByCategory = (category: string): Product[] => {
  if (category === 'tops') {
    return products.filter(p => 
      p.category === 'tops' || 
      ['hoodie', 'sweatshirt', 'sweater', 't shirt', 't-shirt', 'jacket'].includes(p.sub_category?.toLowerCase() || '')
    );
  }
  if (category === 'bottoms') {
    return products.filter(p => 
      p.category === 'bottoms' || 
      ['pants', 'sweatpants', 'joggers', 'jeans', 'chinos'].includes(p.sub_category?.toLowerCase() || '')
    );
  }
  if (category === 'footwear') {
    return products.filter(p => p.sector === 'footwear');
  }
  if (category === 'accessories') {
    return products.filter(p => 
      p.sector === 'accessories' || 
      p.category === 'accessories'
    );
  }
  return [];
};

export const getProductById = (id: string): Product | undefined => {
  return products.find(p => p.sku_id === id);
};
